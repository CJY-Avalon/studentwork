package com.example.demo.domain;

public class studentscott {
    private int id;
    private String name;
    private  int Scott;
    private  int maths;
    private  int eng;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getScott() {
        return Scott;
    }

    public int getMaths() {
        return maths;
    }

    public int getEng() {
        return eng;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setScott(int scott) {
        Scott = scott;
    }

    public void setMaths(int maths) {
        this.maths = maths;
    }

    public void setEng(int eng) {
        this.eng = eng;
    }
}
